    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
        String merk = txtMerk.getText().trim();
        String warna = txtWarna.getText().trim();
        String tahun = txtTahun.getText().trim();
        String bahanBakar = txtBahanBakar.getText().trim();
        int harga = Integer.parseInt(txtHarga.getText().trim());
        int stok = Integer.parseInt(txtStok.getText().trim());
        int jumlahBeli = Integer.parseInt(txtJumlahBeli.getText().trim());

        if (jumlahBeli <= 0) {
            taStruk.setText("Jumlah beli harus lebih dari 0!");
            return;
        }

        Dealermotor motor;
        if (merk.equalsIgnoreCase("Honda")) {
            motor = new Honda(merk, warna, tahun, bahanBakar, harga, stok);
        } else if (merk.equalsIgnoreCase("Yamaha")) {
            motor = new Yamaha(merk, warna, tahun, bahanBakar, harga, stok);
        } else {
            taStruk.setText("Merk tidak dikenal! Gunakan Honda atau Yamaha.");
            return;
        }

        PembayaranDealer pembayaran = new PembayaranDealer(motor, jumlahBeli);

        // Buat struk pembayaran dan tampilkan di taStruk
        StringBuilder sb = new StringBuilder();
        sb.append("===== STRUK PEMBAYARAN =====\n");
        sb.append("Merk            : ").append(pembayaran.motor.getMerk()).append("\n");
        sb.append("Harga per Unit  : Rp ").append(pembayaran.motor.getHarga()).append("\n");
        sb.append("Jumlah Beli     : ").append(jumlahBeli).append("\n");
        sb.append("Total Awal      : Rp ").append(pembayaran.totalAwal).append("\n");
        sb.append("Diskon          : ").append((int)(pembayaran.diskon * 100)).append("%\n");
        sb.append("Total Bayar     : Rp ").append(pembayaran.totalSetelahDiskon).append("\n");

        taStruk.setText(sb.toString());

    } catch (NumberFormatException ex) {
        taStruk.setText("Input harga, stok, dan jumlah beli harus angka valid!");
    }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void BayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BayarActionPerformed
        // TODO add your handling code here
        try {
        int uangBayar = Integer.parseInt(txtUangBayar.getText().trim());
        String merk = txtMerk.getText().trim();
        String warna = txtWarna.getText().trim();
        String tahun = txtTahun.getText().trim();
        String bahanBakar = txtBahanBakar.getText().trim();
        int harga = Integer.parseInt(txtHarga.getText().trim());
        int stok = Integer.parseInt(txtStok.getText().trim());
        int jumlahBeli = Integer.parseInt(txtJumlahBeli.getText().trim());

        Dealermotor motor;
        if (merk.equalsIgnoreCase("Honda")) {
            motor = new Honda(merk, warna, tahun, bahanBakar, harga, stok);
        } else if (merk.equalsIgnoreCase("Yamaha")) {
            motor = new Yamaha(merk, warna, tahun, bahanBakar, harga, stok);
        } else {
            taStruk.setText("Merk tidak dikenal! Gunakan Honda atau Yamaha.");
            return;
        }

        PembayaranDealer pembayaran = new PembayaranDealer(motor, jumlahBeli);

        // Proses pembayaran
        StringBuilder sb = new StringBuilder();
        sb.append("Uang Dibayar    : Rp ").append(uangBayar).append("\n");
        if (uangBayar < pembayaran.totalSetelahDiskon) {
            sb.append("âŒ Uang kurang Rp ").append(pembayaran.totalSetelahDiskon - uangBayar);
        } else {
            sb.append("âœ… Pembayaran berhasil. Kembalian: Rp ").append(uangBayar - pembayaran.totalSetelahDiskon);
            // Update stok motor setelah berhasil bayar
            motor.kurangiStok(jumlahBeli);
            txtStok.setText(String.valueOf(motor.getJumlah())); // update stok di GUI
        }

        taStruk.setText(sb.toString());

    } catch (NumberFormatException ex) {
        taStruk.setText("Input uang bayar harus angka valid!");
    }
    }//GEN-LAST:event_BayarActionPerformed

